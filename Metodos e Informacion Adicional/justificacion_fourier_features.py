"""## 🔬 Parte 4: Separabilidad espectral por canal HSV

Revisamos si el patrón espectral es consistente en los 3 canales (Hue, Saturation, Value) usando el estadístico KS (Kolmogorov-Smirnov) igual que en el notebook principal.
"""

def extract_fft_features_full(img_rgb):
    """Extrae mean, std, max del espectro de cada canal HSV."""
    img_hsv = mcolors.rgb_to_hsv(img_rgb / 255.0)
    feats = {}
    for i, name in enumerate(['Hue', 'Sat', 'Val']):
        fft    = np.fft.fft2(img_hsv[:, :, i])
        mag    = np.abs(np.fft.fftshift(fft))
        log_m  = np.log1p(mag)
        feats[f'FFT_mean_{name}'] = log_m.mean()
        feats[f'FFT_std_{name}']  = log_m.std()
        feats[f'FFT_max_{name}']  = log_m.max()
    return feats

def collect_features(directory, label, max_imgs=200):
    files  = [f for f in os.listdir(directory)
              if f.lower().endswith(('.png','.jpg','.jpeg'))][:max_imgs]
    rows   = []
    for fname in files:
        try:
            img  = load_img(os.path.join(directory, fname))
            feats = extract_fft_features_full(img)
            feats['label'] = label
            rows.append(feats)
        except: pass
    return rows

