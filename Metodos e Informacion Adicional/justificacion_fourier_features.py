def radial_profile(spectrum):
    """Promedia el espectro por banda de frecuencia radial."""
    h, w = spectrum.shape
    cy, cx = h // 2, w // 2
    y, x   = np.ogrid[:h, :w]
    r      = np.sqrt((x - cx)**2 + (y - cy)**2).astype(int)
    max_r  = min(cy, cx)
    profile = np.array([spectrum[r == i].mean() if (r == i).any() else 0
                        for i in range(max_r)])
    return profile

