def load_img(path, size=IMG_SIZE):
    """Carga imagen y la redimensiona."""
    return np.array(Image.open(path).convert('RGB').resize(size,Image.NEAREST))

def get_fourier_spectrum(img_rgb, channel_idx):

    img_hsv = mcolors.rgb_to_hsv(img_rgb / 255.0)
    channel = img_hsv[:, :, channel_idx]
    fft     = np.fft.fft2(channel)
    mag     = np.abs(np.fft.fftshift(fft))
    return np.log1p(mag)

def get_all_spectra(img_rgb):
    """Retorna espectros de los 3 canales HSV."""
    img_hsv = mcolors.rgb_to_hsv(img_rgb / 255.0)
    spectra = {}
    for i, name in enumerate(['Hue', 'Sat', 'Val']):
        fft = np.fft.fft2(img_hsv[:, :, i])
        mag = np.abs(np.fft.fftshift(fft))
        spectra[name] = np.log1p(mag)
    return spectra

