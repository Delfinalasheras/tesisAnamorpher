"""## ✅ Resumen de la justificación

Este análisis muestra que la Transformada de Fourier es una feature justificada para el detector de Anamorpher porque:

1. **Visualmente:** el downsampling bicubic/bilinear introduce artefactos espectrales sistemáticos visibles en el espectro de magnitud FFT pero imperceptibles en el espacio de píxeles.

2. **Estadísticamente:** el estadístico KS mide la separabilidad entre distribuciones de features FFT de imágenes clean vs atacadas. Las features con KS > 0.3 indican que las distribuciones son significativamente distintas entre clases.

3. **Geométricamente:** la proyección PCA 2D muestra que el espacio de features FFT tiene estructura separable, lo que le da al SVM margen para encontrar un hiperplano discriminativo.

4. **Complementariedad:** junto con las features HSI, la FFT aporta información del dominio frecuencial que las estadísticas de color no capturan.

> **Referencia conceptual:** este enfoque sigue la línea de trabajos como *"Unmasking the Abnormal Events in Video" (Ionescu et al.)* y análisis de adversarial examples en el dominio frecuencial (Guo et al., 2019), que muestran que ataques y transformaciones introducen patrones de alta frecuencia detectables con FFT.
"""