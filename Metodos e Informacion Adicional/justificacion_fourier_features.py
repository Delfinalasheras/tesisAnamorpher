def compute_mean_spectrum(directory, channel_idx=2, max_imgs=200):
    """Calcula el espectro de Fourier promedio de una clase."""
    files = [f for f in os.listdir(directory)
             if f.lower().endswith(('.png', '.jpg', '.jpeg'))][:max_imgs]
    spectra = []
    for fname in files:
        try:
            img  = load_img(os.path.join(directory, fname))
            spec = get_fourier_spectrum(img, channel_idx)
            spectra.append(spec)
        except Exception as e:
            print(f"Error en {fname}: {e}")
    return np.mean(spectra, axis=0), np.std(spectra, axis=0)

