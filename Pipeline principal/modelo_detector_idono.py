def rgb_to_hsi(image_rgb):
    img = np.array(image_rgb).astype(np.float64) / 255.0
    R, G, B = img[:, :, 0], img[:, :, 1], img[:, :, 2]
    I = (R + G + B) / 3.0
    min_rgb = np.minimum(np.minimum(R, G), B)
    S = np.where(I > 0, 1.0 - (3.0 / (R + G + B + 1e-8)) * min_rgb, 0.0)
    num   = 0.5 * ((R - G) + (R - B))
    den   = np.sqrt((R - G)**2 + (R - B) * (G - B)) + 1e-8
    theta = np.arccos(np.clip(num / den, -1.0, 1.0))

    H = np.where(B <= G, theta, 2 * np.pi - theta)
    H = H / (2 * np.pi)

    return H, S, I

def HSI_feature(img_rgb: np.ndarray) -> list:
    img_pil = Image.fromarray(img_rgb.astype(np.uint8))
    H, S, I = rgb_to_hsi(img_pil)

    feats = []
    for canal in [H,S,I]:
        flat = canal.flatten()
        feats += [
          float(flat.mean()),
          float(flat.std()),
        ]

    return feats

