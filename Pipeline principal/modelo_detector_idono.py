from PIL import Image
import numpy as np
import matplotlib.pyplot as plt

def downsample_como_anamorpher(img_path, target_size=(512, 512)):
    img = Image.open(img_path).convert('RGB')
    img_down = img.resize(target_size,  Image.NEAREST)

    return img_down


def mostrar_downsampled(img_path, target_size=(512, 512), method='nearest'):
    img_orig = Image.open(img_path).convert('RGB')
    img_down = downsample_como_anamorpher(img_path, target_size, method)

    fig, axes = plt.subplots(1, 2, figsize=(10, 5))

    axes[0].imshow(img_orig)
    axes[0].set_title(f"Original  {img_orig.size[0]}×{img_orig.size[1]}", fontsize=11)
    axes[0].axis('off')

    axes[1].imshow(img_down)
    axes[1].set_title(f"Downsampled ({method})  {target_size[0]}×{target_size[1]}", fontsize=11)
    axes[1].axis('off')

    plt.tight_layout()
    plt.show()

    return img_down

def predecir_imagen(img_path, modelo, target_size=(255, 255)):
    """
    Hace downsampling igual que Anamorpher y predice con el modelo entrenado.
    """
    # 1. Downsampling como Anamorpher
    img_down = downsample_como_anamorpher(img_path, target_size)
    img_rgb  = np.array(img_down)

    # 2. Extraer features (mismo pipeline que en el dataset)
    feat_fft = FFT_features(img_rgb)
    feat_hsi = HSI_feature(img_rgb)
    feat     = np.array(list(feat_fft) + list(feat_hsi), dtype=np.float32).reshape(1, -1)

    # 3. Predecir
    pred = modelo.predict(feat)[0]
    prob = modelo.predict_proba(feat)[0]
    etiqueta = "CLEAN" if pred == 0 else "ATAQUE (interpolada)"

    # 4. Mostrar
    fig, axes = plt.subplots(1, 2, figsize=(10, 5))
    img_orig = Image.open(img_path).convert('RGB')
    axes[0].imshow(img_orig)
    axes[0].set_title("Original", fontsize=11)
    axes[0].axis('off')

    axes[1].imshow(img_down)
    color = 'red' if pred == 1 else 'green'
    axes[1].set_title(
        f"{etiqueta}\nP(clean)={prob[0]:.3f}  P(ataque)={prob[1]:.3f}",
        fontsize=10, color=color
    )
    axes[1].axis('off')
    plt.tight_layout()
    plt.show()

    return {"label": etiqueta, "pred": pred, "proba": prob}

