import matplotlib.colors as mcolors
def FFT_features(img_rgb):
    img_hsv = mcolors.rgb_to_hsv(np.array(img_rgb) / 255.0)
    v_channel = img_hsv[:,:,2]
    feat_fft = []
    for i, name in enumerate(['Hue', 'Sat', 'Val']):
        channel = img_hsv[:,:,i]
        fft = np.fft.fft2(channel)
        mag = np.abs(np.fft.fftshift(fft))
        log_mag = np.log1p(mag)

        feat_fft += [
            log_mag.mean(),
            log_mag.std(),
            np.max(log_mag)
        ]
    return np.array(feat_fft)

"""# CARGAR IMAGENES Y EXTRAER FEATURES"""

def extract_features(img_path: str) -> np.ndarray:
    img_rgb = np.array(Image.open(img_path).convert('RGB').resize((255,255)))
    feat_fft = FFT_features(img_rgb)
    feat_hsi = HSI_feature(img_rgb)
    feature_vector = np.array( list(feat_fft) + list(feat_hsi),
    dtype=np.float32
)

    return feature_vector

def load_dataset(base_path,suffix=""):
    X, y, groups = [], [], []
    n=0
    class_map = {
        f"clean{suffix}":    0,
        f"bicubic{suffix}":  1,
        f"bilinear{suffix}": 1,
    }

    for folder, label in class_map.items():
        folder_path = os.path.join(base_path, folder)
        if not os.path.exists(folder_path):
            print(f"NO: {folder_path}")
            continue

        files = [f for f in os.listdir(folder_path)
                 if f.lower().endswith(('.png', '.jpg', '.jpeg'))]

        for fname in files:
            fpath = os.path.join(folder_path, fname)
            print(fpath)
            try:
                feat = extract_features(fpath)
                X.append(feat)
                y.append(label)
                groups.append(folder)
                n=n+1
            except Exception as e:
                print(f"Error con {fname}: {e}")
    print(n)

    return np.array(X), np.array(y), groups
