"""MEDIAN BLUR"""

def model_robust_median(image_path, output_folder=SANITIZADAS, ksize=5):
    img       = cv2.imread(image_path)
    sanitized = cv2.medianBlur(img, ksize)
    out = os.path.join(output_folder, f"median_{os.path.basename(image_path)}")
    cv2.imwrite(out, sanitized)
    return {'sanitized': sanitized, 'out_path': out, 'ksize': ksize}

