def rgb_to_hsi(image_rgb):
    img = np.array(image_rgb).astype(np.float64) / 255.0
    R, G, B = img[:, :, 0], img[:, :, 1], img[:, :, 2]
    I = (R + G + B) / 3.0
    min_rgb = np.minimum(np.minimum(R, G), B)
    S = np.where(I > 0, 1.0 - (3.0 / (R + G + B + 1e-8)) * min_rgb, 0.0)
    num   = 0.5 * ((R - G) + (R - B))
    den   = np.sqrt((R - G) ** 2 + (R - B) * (G - B)) + 1e-8
    theta = np.arccos(np.clip(num / den, -1.0, 1.0))
    H = np.where(B <= G, theta, 2 * np.pi - theta) / (2 * np.pi)
    return H, S, I


def HSI_feature(img_rgb):
    H, S, I = rgb_to_hsi(Image.fromarray(img_rgb.astype(np.uint8)))
    feats = []
    for canal in [H, S, I]:
        flat = canal.flatten()
        feats += [float(flat.mean()), float(flat.std())]
    return feats


def FFT_features(img_rgb):
    img_hsv = mcolors.rgb_to_hsv(np.array(img_rgb) / 255.0)
    feat_fft = []
    for i in range(3):
        channel = img_hsv[:, :, i]
        mag     = np.abs(np.fft.fftshift(np.fft.fft2(channel)))
        log_mag = np.log1p(mag)
        feat_fft += [log_mag.mean(), log_mag.std(), np.max(log_mag)]
    return np.array(feat_fft)


def extract_features(img_path):
    """Mismo preprocesamiento que en el entrenamiento: resize a 255x255."""
    img_rgb = np.array(Image.open(img_path).convert('RGB').resize((255, 255)))
    return np.array(list(FFT_features(img_rgb)) + list(HSI_feature(img_rgb)),
                    dtype=np.float32)

"""OCR + CONSENSO"""

def preprocess_for_ocr(pil_img, mode='raw'):
    img = np.array(pil_img)
    if mode == 'adaptive':
        hsv = cv2.cvtColor(img, cv2.COLOR_RGB2HSV)
        h, s, v = cv2.split(hsv)
        s = np.clip(cv2.multiply(s, 3), 0, 255).astype(np.uint8)
        img_sat = cv2.cvtColor(cv2.merge([h, s, v]), cv2.COLOR_HSV2RGB)
        img_exp = cv2.convertScaleAbs(img_sat, alpha=2.5, beta=50)
        gray = cv2.cvtColor(img_exp, cv2.COLOR_RGB2GRAY)
        clahe = cv2.createCLAHE(clipLimit=3.0, tileGridSize=(8, 8))
        enhanced = clahe.apply(gray)
        return cv2.adaptiveThreshold(enhanced, 255, cv2.ADAPTIVE_THRESH_GAUSSIAN_C,
                                     cv2.THRESH_BINARY, 11, 2)
    return img



def downscale_and_ocr_data(image_file, target_size, scale_method='bilinear',
                           prep_mode='adaptive', min_conf=30):
    img = Image.open(image_file).convert('RGB')
    if isinstance(target_size, int):
        target_size = (target_size, target_size)
    methods = {'bilinear': Image.Resampling.BILINEAR,
               'bicubic':  Image.Resampling.BICUBIC,
               'nearest':  Image.Resampling.NEAREST}
    downscaled = img.resize(target_size, resample=methods[scale_method])
    processed  = preprocess_for_ocr(downscaled, mode=prep_mode)
    H, W = processed.shape[:2]
    data = pytesseract.image_to_data(processed, config=r'--oem 3 --psm 11',
                                     output_type=Output.DICT)
    words = []
    for i in range(len(data['text'])):
        try:   conf = int(data['conf'][i])
        except ValueError: conf = -1
        w = data['text'][i].strip()
        if conf > min_conf and w and any(c.isalnum() for c in w):
            x, y, ww, hh = (data['left'][i], data['top'][i],
                            data['width'][i], data['height'][i])
            words.append({'word': w, 'conf': conf,
                          'cx': (x + ww / 2) / W, 'cy': (y + hh / 2) / H,
                          'box_norm': (x / W, y / H, ww / W, hh / H)})
    return {'image': processed, 'size': target_size, 'method': scale_method, 'words': words}


def _lev(a, b):
    if _lev_lib is not None:
        return _lev_lib(a, b)
    m, n = len(a), len(b)
    if not m: return n
    prev = list(range(n + 1))
    for i in range(1, m + 1):
        cur = [i] + [0] * n
        for j in range(1, n + 1):
            cur[j] = min(prev[j] + 1, cur[j - 1] + 1, prev[j - 1] + (a[i - 1] != b[j - 1]))
        prev = cur
    return prev[n]


def _norm(w):
    return re.sub(r'[^A-Z0-9]', '', w.upper())


def consenso_texto(image_path, scale_methods=('bilinear', 'bicubic', 'nearest'),
                   sizes=(255, 512, 800, 1092, 1600), prep_mode='adaptive',
                   min_conf=30, min_votos=4, pos_tol=0.06, sim_tol=0.34, min_len=2):
    # 1. recolectar TODAS las detecciones de las 12 reconstrucciones
    dets = []
    for m in scale_methods:
        for s in sizes:
            for wd in downscale_and_ocr_data(image_path, s, m, prep_mode, min_conf)['words']:
                nw = _norm(wd['word'])
                if nw:
                    dets.append({**wd, 'nword': nw, 'src': (m, s)})

    # 2. agrupar por palabra parecida + posicion cercana (semilla = mayor conf)
    grupos = []
    for d in sorted(dets, key=lambda x: -x['conf']):
        for g in grupos:
            cerca = abs(d['cx'] - g['cx']) < pos_tol and abs(d['cy'] - g['cy']) < pos_tol
            ml = max(len(d['nword']), len(g['rep']))
            igual = (ml and _lev(d['nword'], g['rep']) / ml <= sim_tol)
            if cerca and igual:
                g['items'].append(d); g['srcs'].add(d['src'])
                g['cx'] = np.mean([it['cx'] for it in g['items']])
                g['cy'] = np.mean([it['cy'] for it in g['items']])
                break
        else:
            grupos.append({'rep': d['nword'], 'cx': d['cx'], 'cy': d['cy'],
                           'items': [d], 'srcs': {d['src']}})

    # 3. quedarse con los votados por >= min_votos reconstrucciones distintas
    fuertes = [g for g in grupos if len(g['srcs']) >= min_votos]
    for g in fuertes:
        g['palabra']  = Counter(it['nword'] for it in g['items']).most_common(1)[0][0]
        g['votos']    = len(g['srcs'])
        g['conf']     = float(np.mean([it['conf'] for it in g['items']]))
        g['box_norm'] = tuple(np.median(np.array([it['box_norm'] for it in g['items']]), axis=0))

    # 3.b descartar "letras sueltas": tokens de 1 caracter son ruido del fondo
    #     texturado (votan alto por ser consistentes, no porque sean texto real)
    fuertes = [g for g in fuertes if len(g['palabra']) >= min_len]

    # 4. ordenar en lectura: por renglon (cy) y luego por x
    fuertes.sort(key=lambda g: (round(g['cy'] / 0.05), g['cx']))
    return {'texto': ' '.join(g['palabra'] for g in fuertes),
            'palabras': [{'palabra': g['palabra'], 'votos': g['votos'],
                          'conf': round(g['conf'], 1),
                          'box_norm': tuple(np.round(g['box_norm'], 3))} for g in fuertes]}

