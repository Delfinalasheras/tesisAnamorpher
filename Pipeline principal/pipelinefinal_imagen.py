"""# EJECUCION PIPELINE"""

def pipeline(image_path, modelo, vista_metodo='nearest', vista_size=(512, 512)):
    print("=" * 80)
    print("IMAGEN:", os.path.basename(image_path))

    # --- PASO 2: DETECTOR ---
    feat = extract_features(image_path).reshape(1, -1)
    pred = int(modelo.predict(feat)[0])
    prob = modelo.predict_proba(feat)[0]
    etiqueta = "CLEAN" if pred == 0 else "ATAQUE (interpolada)"
    print(f"DETECTOR -> {etiqueta}   P(clean)={prob[0]:.3f}  P(ataque)={prob[1]:.3f}")
    mostrar_deteccion(image_path, etiqueta, prob, vista_size, vista_metodo)

    # --- PASO 3: RAMA SEGUN DETECCION ---
    if pred == 1:                                  # 3.1 ATAQUE
        res = consenso_texto(image_path)
        if res['palabras']:                        # 3.1.a hay texto -> consenso + inpaint
            print(f"OCR -> texto detectado: {res['texto']}")
            mostrar_consenso(image_path, res)
            san = sanitizar_por_consenso(image_path, res)

            if san['rechazada']:
                print(f"IMAGEN RECHAZADA: el texto oculto cubre {san['cobertura']*100:.0f}% "
                      f"del frame. No es recuperable -> se descarta en lugar de inpaint.")
                plt.imshow(san['mask'], cmap='gray'); plt.axis('off')
                plt.title(f"Mascara de rechazo ({san['cobertura']*100:.0f}% cobertura)"); plt.show()
            else:
                mostrar_sanitizado(image_path, san)
                res_post = consenso_texto(san['out_path'])
                print(f"  cobertura tapada: {san['cobertura']*100:.0f}%")
                print(f"  antes:   {res['texto']}")
                print(f"  despues: {res_post['texto'] or '(ninguno) OK'}")
        else:                                      # 3.1.b sin texto recuperable -> median blur
            print("OCR -> el consenso no recupero texto. Fallback: median blur global.")
            san = model_robust_median(image_path)
            mostrar_median(image_path, san)
    else:                                          # 3.2 NO ATAQUE -> grid
        print("Imagen CLEAN. Mostrando grid de reconstrucciones.")
        mostrar_grid_ocr(image_path)

    print("=" * 80)
    return {'pred': pred, 'etiqueta': etiqueta, 'proba': prob.tolist()}

