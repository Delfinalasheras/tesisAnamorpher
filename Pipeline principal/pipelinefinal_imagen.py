def downsample_revelado(img_path, target_size=(800, 800), method='bilinear'):
    methods = {'nearest':  Image.Resampling.NEAREST,
               'bilinear': Image.Resampling.BILINEAR,
               'bicubic':  Image.Resampling.BICUBIC}
    return Image.open(img_path).convert('RGB').resize(target_size, methods[method])


def realzar_para_lectura(pil_img, sat_mult=3, alpha=2.5, beta=50):
    """Realza la escalada SIN binarizar -> deja el texto oculto legible a color.
       Es el mismo front de preprocess_for_ocr('adaptive') (saturacion + exposicion)
       pero cortando antes del gris/CLAHE/threshold, para que se vea como lo lee el ojo."""
    img = np.array(pil_img)                          # RGB
    hsv = cv2.cvtColor(img, cv2.COLOR_RGB2HSV)
    h, s, v = cv2.split(hsv)
    s = np.clip(cv2.multiply(s, sat_mult), 0, 255).astype(np.uint8)   # +saturacion
    img_sat = cv2.cvtColor(cv2.merge([h, s, v]), cv2.COLOR_HSV2RGB)
    img_exp = cv2.convertScaleAbs(img_sat, alpha=alpha, beta=beta)    # +exposicion/contraste
    return img_exp


def mostrar_deteccion(img_path, etiqueta, prob, target_size=(512, 512), method='nearest'):
    """Paso 2: original | escalada (revela el ataque) | escalada realzada (texto legible)."""
    orig     = Image.open(img_path).convert('RGB')
    rev      = downsample_revelado(img_path, target_size, method)
    realzada = realzar_para_lectura(rev)

    fig, ax = plt.subplots(1, 3, figsize=(16, 5))
    ax[0].imshow(orig); ax[0].set_title(f"Original  {orig.size[0]}x{orig.size[1]}")

    color = 'red' if etiqueta.startswith('ATAQUE') else 'green'
    ax[1].imshow(rev)
    ax[1].set_title(f"Escalada {method} {target_size[0]}x{target_size[1]}\n"
                    f"{etiqueta}   P(clean)={prob[0]:.3f}  P(ataque)={prob[1]:.3f}",
                    color=color)

    ax[2].imshow(realzada)
    ax[2].set_title("Realzada (saturacion x3 + exposicion)\ntexto legible")

    for a in ax:
        a.axis('off')
    plt.tight_layout(); plt.show()

""" OCR"""

def mostrar_consenso(image_path, res, sobre=('bilinear', 512)):
    """Dibuja las cajas del consenso (en rojo) con la palabra y la cantidad de votos.
       sobre=('metodo', size) -> sobre una reconstruccion (se ve el texto).
       sobre=None             -> sobre la imagen ORIGINAL (lo que sanitiza el pipeline)."""
    if sobre is None:
        img = np.array(Image.open(image_path).convert('RGB'))
    else:
        m, s = sobre
        proc = downscale_and_ocr_data(image_path, s, m)['image']
        img  = cv2.cvtColor(proc, cv2.COLOR_GRAY2RGB) if proc.ndim == 2 else proc.copy()

    H, W = img.shape[:2]
    vis = img.copy()
    th = max(1, W // 500); fs = max(0.4, W / 1500)
    for p in res['palabras']:
        x, y, w, h = p['box_norm']
        x0, y0, x1, y1 = int(x * W), int(y * H), int((x + w) * W), int((y + h) * H)
        cv2.rectangle(vis, (x0, y0), (x1, y1), (255, 0, 0), th)
        cv2.putText(vis, f"{p['palabra']}({p['votos']})", (x0, max(12, y0 - 4)),
                    cv2.FONT_HERSHEY_SIMPLEX, fs, (255, 0, 0), th, cv2.LINE_AA)

    plt.figure(figsize=(8, 8))
    plt.imshow(vis); plt.axis('off')
    plt.title(f"Consenso: {res['texto']}")
    plt.show()

def mostrar_grid_ocr(image_path, scale_methods=('bilinear', 'bicubic', 'nearest'),
                     sizes=(255, 512, 800, 1092), prep_mode='adaptive',
                     usar_error=False, umbral_error=80):
    resultados = []
    for method in scale_methods:
        for size in sizes:
            r    = downscale_and_ocr_data(image_path, size, scale_method=method, prep_mode=prep_mode)
            text = r['text']
            if usar_error:
                continue
            resultados.append({'img': r['image'],
                               'title': f"{method.upper()}\n{size}px",
                               'method': method, 'size': size,
                               'status': (text[:40].replace('\n', ' ') if text else "---"),
                               })
    n = len(resultados)
    if n == 0:
        print("Sin reconstrucciones para mostrar."); return
    fig, axes = plt.subplots(1, n, figsize=(n * 3, 3))
    if n == 1:
        axes = [axes]
    for ax, res in zip(axes, resultados):
        ax.imshow(res['img'], cmap='gray'); ax.set_title(res['title'], fontsize=9); ax.axis('off')
    plt.tight_layout(); plt.show()

    header = f"{'METODO':<10} | {'TAMANO':<8} | {'TEXTO ENCONTRADO':<42}"
    if usar_error: header += " | ERROR"
    print(header)
    print("-" * len(header))
    for res in resultados:
        line = f"{res['method'].upper():<10} | {str(res['size']):<8} | {res['status']:<42}"
        if usar_error: line += f" | {res['error']:.1f}"
        print(line)

