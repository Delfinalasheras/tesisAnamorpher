def filas_dominantes(palabras, y_tol=0.04, min_frac=0.5):
    """Agrupa palabras en renglones (por cy) y conserva los que concentran la mayoria
       de los votos -> filtra detecciones sueltas."""
    rows = []
    for p in sorted(palabras, key=lambda q: q['box_norm'][1]):
        y = p['box_norm'][1]
        for r in rows:
            if abs(r['cy'] - y) < y_tol:
                r['items'].append(p)
                r['cy'] = np.mean([it['box_norm'][1] for it in r['items']]); break
        else:
            rows.append({'cy': y, 'items': [p]})
    for r in rows:
        r['votos'] = sum(it['votos'] for it in r['items'])
    if not rows: return []
    maxv = max(r['votos'] for r in rows)
    return [r for r in rows if r['votos'] >= min_frac * maxv]

def sanitizar_por_consenso(image_path, res, output_folder=SANITIZADAS,
                           method='telea', pad_frac=0.03, radius=3,
                           max_cobertura=0.45):
    img = cv2.imread(image_path); H, W = img.shape[:2]
    mask = np.zeros((H, W), np.uint8); pad = int(pad_frac * H)

    filas = filas_dominantes(res['palabras'])
    if filas:
        filas = sorted(filas, key=lambda r: r['cy'])
        bloques = [[filas[0]]]
        for r in filas[1:]:
            (bloques[-1].append(r) if abs(r['cy'] - bloques[-1][-1]['cy']) < 0.08
              else bloques.append([r]))
        for b in bloques:
            tops = [it['box_norm'][1] for r in b for it in r['items']]
            bots = [it['box_norm'][1] + it['box_norm'][3] for r in b for it in r['items']]
            y0 = max(0, int(min(tops)*H) - pad); y1 = min(H, int(max(bots)*H) + pad)
            mask[y0:y1, :] = 255

    cobertura = float((mask > 0).mean())          # fraccion de imagen que se taparia

    # --- GATE: si el texto domina la imagen, no tiene sentido reconstruir ---
    if cobertura > max_cobertura:
        return {'sanitized': None, 'mask': mask, 'out_path': None,
                'cobertura': cobertura, 'rechazada': True}

    flag = cv2.INPAINT_TELEA if method == 'telea' else cv2.INPAINT_NS
    sanitized = cv2.inpaint(img, mask, radius, flag) if mask.sum() else img.copy()
    out = os.path.join(output_folder, f"clean_{os.path.basename(image_path)}")
    cv2.imwrite(out, sanitized)
    return {'sanitized': sanitized, 'mask': mask, 'out_path': out,
            'cobertura': cobertura, 'rechazada': False}


def mostrar_sanitizado(image_path, san, vista=('bilinear', 512)):
    m, s = vista
    orig = cv2.cvtColor(cv2.imread(image_path), cv2.COLOR_BGR2RGB)
